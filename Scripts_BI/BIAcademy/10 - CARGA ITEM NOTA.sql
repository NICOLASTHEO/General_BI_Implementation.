/* CRIANDO UMA VIEW PARA VERIFICAR OS ITENS DE NOTA PEDIDOS */

CREATE VIEW V_ITEM_NOTA AS
SELECT ID_NOTA_FISCAL AS "NOTA FISCAL",
	   PRODUTO, 
	   VALOR, 
       QUANTIDADE,
	   TOTAL AS "TOTAL DO ITEM"
FROM PRODUTO
INNER JOIN ITEM_NOTA
ON IDPRODUTO = ID_PRODUTO
GO

